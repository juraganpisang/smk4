					<span ></span>&nbsp;&nbsp;&nbsp;&nbsp;SMK Negeri 4 Malang adalah Sekolah Menengah Kejuruan Negeri yang ada di Malang yang beralamat di Jl. Tanimbar 22 Malang. Sekolah ini mempunyai 6 jurusan, yaitu Produksi Grafika, Persiapan Grafika, Multimedia, Rekayasa Perangkat Lunak, Animasi, dan TKJ. Jumlah siswa di sekolah ini sekitar 3300 siswa dengan rincian setiap tingkatan kelas terdiri dari 1100 siswa.<br/>
					<span ></span>&nbsp;&nbsp;&nbsp;&nbsp;Pada tahun 1938 misi Gereja Katolik dibawah Keuskupan Malang mendirikan Sekolah Teknik Pertama Percetakan (Grafisce School) di Malang yang dipimpin langsung oleh Mrg. Aliers, O.Carm yang bertempat di jalan Frateran No.21 Malang yang sekarang menjadi Jl J.A. Suprapto No. 21 Malang dengan masa studi 2 tahun. Sedangkan yang ditunjuk sebagai Kepala Sekolah pada waktu itu adalah Fr. Cicilianus H.C.A Lommelaars.<br/>
					<span ></span>Pada tahun 1953 Sekolah Sekolah Teknik Pertama Percetakan ini diganti menjadi Sekolah Kerajinan Negeri (SKN) dengan masa studi 3 tahun yang menerima siswa baru dari lulusan Sekolah Rakyat (SR).</br>
					<ul style="text-align:justify">
						<li>Tahun 1954 Sekolah Kerajinan Negeri (SKN) Malang diganti menjadi Sekolah Teknik Menengah Bagian Percetakan STM Bagian Percetakan dengan masa studi selama 3 tahun. Oleh Keuskupan Malang sebagai Kepala Sekolah pada waktu itu ditunjuk <i>Fr. Nolascus Waijers</i> menggantikan Kepala Sekolah yang lama, bersama dengan itu lokasi sekolah juga pindah ke jalan Bengawan Solo No.38 yang sekarang diganti menjadi Jl. R. Tumenggung Soerjo No. 38 Malang.</li><br/>
						<li>Tahun 1957 Sekolah Teknik Menengah Bagian Percetakan diganti lagi menjadi Sekolah Guru Pendidikan Teknik Percetakan (SGPT Percetakan)</li><br/>
						<li>Tahun 1959 diganti menjadi Sekolah Menengah Teknik Grafika Malang (SMT Grafika) dan ditunjuk sebagai Kepala Sekolah waktu itu M.Sultany Arief.</li><br/>
						<li>Tahun 1996 SMT Grafika berubah nama menjadi Sekolah Menengah Kejuruan Negeri 4 Malang (SMKN 4 Malang), bersama dengan itu lokasi juga pindah dari Jl. R. Tumenggung Soerjo No. 38 Malang ke Jl Tanimbar No. 22 Malang.</li><br/>
						<li>Tahun 2002 SMK Negeri 4 Malang membuka bidang keahlian baru yakni Teknologi Informasi (TI).</li><br/>
						<li>Tahun 2006 SMK Negeri 4 Malang membuka bidang keahlian baru Animasi.</li><br/>
						<li>Tahun 2008 Meraih ISO 9001 : 2000 Sistem Manajemen Mutu.</li>
