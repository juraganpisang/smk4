Sunday Krunch digagas oleh Mugni Gustian/Tyan (gitar) dan Muhammad Akbar Refriansyah/Akbar (drum), dua personel dari Super-K, band anak-anak yang pernah populer dengan singel "Playgirl".

Terbentuknya Sunday Krunch dipicu oleh adanya pergantian formasi yang terjadi karena vokalis mereka mengundurkan diri pada tahun 2012. Kondisi ini membuat Tyan, Akbar dan Patrick Effendy (founder dan produser) berinisiatif mengadakan audisi untuk mencari vokalis juga bassist.

Usaha mereka menuai hasil. Defta, teman lama Tyan dan Akbar bergabung di akhir 2012 menjadi bassist. Indah pun menyusul sebagai vokalis menambah line up SUPER-K formasi baru ini.

Dengan formasi teranyar ini, tepat di tanggal 2 Maret 2013, kuartet ini lantas berganti nama menjadi SUNDAY KRUNCH. Nama ini dipilih karena sesuai dengan cerminan dari karakter musik mereka, yaitu musik untuk bersenang-senang, dengan warna musik pop rock (crunch).

SUNDAY KRUNCH hadir dengan konsep bermusik yang jujur, analog, dengan tema-tema lagu yang membahas tentang kehidupan keseharian mereka. Di usia mereka yang masih terbilang sangat belia 15-17 tahun, mereka mencoba berkarya dan bercerita tentang keseharian di sekitar mereka tentang cinta “muda”.

Hal ini ditunjukkan dengan single pertama mereka yang bertajuk PLAYBOY. Single yang ditulis oleh Patrick Effendy ini bercerita dengan pengalaman keseharian sang vokalis, Indah, tentang cowok-cowok tipikal Playboy yang mencoba mendekatinya.

“Untuk musiknya sendiri, pastinya beda dengan SUPER-K, “ ungkap mereka tentang musik SUNDAH KRUNCH. Namun menurut mereka, single PLAYBOY ini dipilih untuk memuaskan rasa kangen semua SUPERFRIENDZ (fans) yang ada di Indonesia. (sevenmusic)


